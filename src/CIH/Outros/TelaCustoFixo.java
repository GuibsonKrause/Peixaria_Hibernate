    private void tableCadastradosMouseClicked(java.awt.event.MouseEvent evt)//GEN-FIRST:event_tableCadastradosMouseClicked
    {//GEN-HEADEREND:event_tableCadastradosMouseClicked
        int linha = tableCadastrados.getSelectedRow();

        try
        {
            custoFixoSelecionado = (CustoFixo) tableCadastrados.getValueAt(linha, 0);
        } catch (Exception ex)
        {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
        }
        
        preencherCampos(custoFixoSelecionado);
    }//GEN-LAST:event_tableCadastradosMouseClicked

