    private void btnInserirActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnInserirActionPerformed
    {//GEN-HEADEREND:event_btnInserirActionPerformed

        try
        {

            String custoFixo;
            if ("".equals(txtOutros.getText()))
            {
                custoFixo = (String) cbxCustoF.getSelectedItem();

            } else
            {
                custoFixo = txtOutros.getText();
            }
            float preco = (float) spinPreco.getValue();

            SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
            Date data = formato.parse(txtData.getText());

            if (((JButton) evt.getSource()).getMnemonic() == 'C')
            {

                int idCustoFixo = gerCCI.getGerCDP().inserirCustoFixo(custoFixo,
                        preco, data);

                JOptionPane.showMessageDialog(this, "Custo Fixo " + idCustoFixo
                        + " inserido com sucesso.");
            } else
            {
                int idCustoFixo = gerCCI.getGerCDP().alterarCustoFixo(
                        custoFixoSelecionado.getCodigo(), 
                        custoFixoSelecionado.getCustoFixo(), preco, data);

                JOptionPane.showMessageDialog(this, "Custo Fixo " + idCustoFixo
                        + " alterado com sucesso.");
            }

            listarCustoFixo();
        
        } catch (SQLException erro)
        {
            JOptionPane.showMessageDialog(this, "ERRO ao inserir Custo Fixo! "
                    + erro.getMessage());
        } catch (HeadlessException | ClassNotFoundException erro)
        {
            JOptionPane.showMessageDialog(this, "ERRO não previsto! "
                    + erro.getMessage());
        } catch (ParseException ex)
        {
            JOptionPane.showMessageDialog(this, "ERRO " + ex);
        }

    }//GEN-LAST:event_btnInserirActionPerformed

